#La correlación no es causación: revertir causa y efecto

#Otra forma en que las asociaciones se confunden con la causalidad es cuando la causa y el efecto se invierten.

#Un ejemplo de esto es afirmar que la tutoría empeora a los estudiantes porque tienen un rendimiento más bajo que los compañeros que no tienen tutoría.

#Aquí, la tutoría no está causando la prueba baja, sino al revés.

#Una forma de esta afirmación se hizo en realidad en una edición en el New York Times, titulada "La participación de los padres está sobrevaluada".

#Considere esta cita del artículo.

#"Cuando examinamos si la ayuda regular con la tarea tuvo un impacto positivo en el rendimiento académico de los niños, nos sorprendió bastante lo que encontramos.

#Independientemente de la clase social familiar, el origen racial o étnico, o el nivel de grado del niño, la ayuda consistente con la tarea casi nunca mejoró los puntajes o calificaciones de los exámenes.

#Aún más sorprendente para nosotros fue que cuando los padres ayudaban regularmente con la tarea, los niños usualmente se desempeñan peor ".

#Una posibilidad muy probable es que los niños que necesitan ayuda regular de los padres reciban esta ayuda porque no tienen un buen desempeño en la escuela.

